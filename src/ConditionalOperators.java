public class ConditionalOperators {

    public static void main(String[] args) {

        int a = 20;
        int b = 21;

       // System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>=b);
        System.out.println(a>=20); // true
        System.out.println(a>20); //false
        System.out.println(a<20); //false
        System.out.println(a<=20); //true

    }
}
